
#' Solar Orange
#'
#' The hexadecimal code that specifies Pantone 144 ('solar orange'),
#' which corresponds to 233-131-0 in the RGB (red-green-blue) color model.
#'
#' Solar orange is one of the three primary Callier Center colors
#' (solar orange, space blue, callier gray), which should be used when depicting
#' Callier Center colors in print or electronic formats. Of the primary
#' Callier Center colors, solar orange has first precedence, space blue has
#' second, and callier gray has third. Hence, one-color images should use
#' solar orange; two-color images should use solar orange and space blue; and
#' three-color images should use solar orange, space blue, and callier gray.
#'
#' @return A character string, \code{'#E98300'}.
#'
#' @aliases #E98300 #e98300
#'
#' @source Callier Center Graphic Standards Manual,
#'   UT Dallas Brand Standards (\url{https://www.utdallas.edu/brand/palette/})
#'
#' @export
solarOrange <- function() {
  '#E98300'
}

